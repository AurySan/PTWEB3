                  <h3 class="titre-secondaire">La charte de vue du coeur</h3>
                  <p class="texte_fonce">
                  Vue du cœur s’est dotée d’une charte que voilà ci-dessous
                  </p>
                </div>
                <div class="block-hidden">
                  <p class="texte_fonce">
                  « Vue du cœur » a pour vocation de favoriser le développement et l’épanouissement d’enfants déficients visuels et non-voyants :
                </p>
                <ul class="texte_fonce">
                  <li>En pratiquant le «vivre ensemble» entre enfants voyants et non-voyants.</li>
                  <li>En développant leur autonomie et leur mobilité</li>
                  <li>En encourageant leur confiance en eux et à s’ouvrir au monde</li>
                </ul>
                <p class="texte_fonce">
                  Pour ce faire, l´association
                </p>
                <ul class="texte_fonce">
                  <li>recourt aux technologies modernes de navigation pour faire découvrir aux enfants non-voyants l’environnement naturel/urbain</li>
                  <li>organise des sorties communes, de natures diverses (ludiques/culturelles/ sportives…) et  des activités adaptées à l’âge et aux besoins des enfants.</li>
                <p class="texte_fonce">
                  L’association s’appuie sur l’expérience de jeunes adultes déficients visuels et sur des associations de proximité pour accompagner les enfants (Vue d’ensemble et Yvoir)
                </p>
                <p class="texte_fonce">
                  L’association « Vue du cœur »  existe grâce à ses membres bénévoles qui la font vivre et le bénévolat est compris comme un engagement libre, moral et non rétribué de personnes qui agissent pour d’autres ou pour l’intérêt collectif, dans un cadre qui va au-delà de l’entraide familiale ou amicale.
                </p>
                <p class="texte_fonce">
                  La présence d’un des parents  est demandée (ou un représentant mandaté par les parents) lors des manifestations, pour soutenir et encourager les enfants, pour mieux prendre la mesure des progrès accomplis, pour être partie prenante des initiatives prises par l´association.  Les enfants restent sous la responsabilité des parents ou de la personne majeure désignée par les parents.
